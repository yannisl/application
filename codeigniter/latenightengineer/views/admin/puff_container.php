<h3>The Panel Component</h3>

<p style="line-height:1.5">The panel component, enables to create panels programmatically. These can be made and defined in a yaml file and stored for further
use and manipulation. They can contain within their container area, other components as well as datasources.
</p>
<p style="line-height:1.5">
Like normal templates, they can contain variables that can be filtered through the Template and Filter Engine, normally.
All components follow the MVC pattern. Once they are enabled via a datasource they can draw on the database for
data without the need of the parent controller to call these models. Data can also come from othe rsources, such as
a feed, a file or even AJAX enabled. They can have their own css snippet. This way the CSS use is exactly the one that is needed
for a particular page.
</p>

<p class="red-arrow" style="line-height:1.5">

Since they can be cached, the main page is served faster with most of its content being just html and only the
changing portion is not cached. They are also a great way to play around while designing the layout by moving the
blocks around in real time rather than just in photoshop.
